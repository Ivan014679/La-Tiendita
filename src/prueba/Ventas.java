            while(sr.next()){
                Object[] rows =new Object[rsmd.getColumnCount()];
                for (int i = 0; i < rows.length; i++) {
                    rows[i]=sr.getObject(i+1);
                }
                data.add(rows);
            }
             dtm = (DefaultTableModel)this.tablaprocomp.getModel();
