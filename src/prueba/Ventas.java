    private void bminimizar2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bminimizar2MouseClicked
        int i = tablaprocomp.getSelectedRow();

        if (tablaprocomp.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Necesita seleccionar un elemento.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            if (tcantidad.getText().isEmpty()) {

                JOptionPane.showMessageDialog(null, "Especifique la cantidad de unidades.", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (Integer.parseInt(tcantidad.getText()) == 0) {
                JOptionPane.showMessageDialog(null, "La cantidad de unidades debe ser mayor a cero", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                if (Integer.parseInt(tcantidad.getText()) > (Integer) tablaprocomp.getValueAt(i, 2)) {
                    if ((Integer) tablaprocomp.getValueAt(i, 2) == 0) {
                        JOptionPane.showMessageDialog(null, "No hay producto existente.", "Error", JOptionPane.ERROR_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(null, "Solo hay en existencia " + tablaprocomp.getValueAt(i, 2) + " Unidades", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    //tablitamodelo.addRow(new Object[]{(String)(tablaprocomp.getValueAt(i, 1)),Integer.parseInt(tcantidad.getText()),Integer.parseInt((String)(tablaprocomp.getValueAt(i, 3)))});

                    DefaultTableModel tablitamodelo = (DefaultTableModel) tablacompra.getModel();
                    tablitamodelo.addRow(new Object[]{tablaprocomp.getValueAt(i, 0), tablaprocomp.getValueAt(i, 1), tcantidad.getText(), tablaprocomp.getValueAt(i, 3), tablaprocomp.getValueAt(i, 2)});
                    tsiniva = ((int) tablaprocomp.getValueAt(i, 3)) * (Integer.parseInt(tcantidad.getText())) + tsiniva;

                    jTextField16.setText("" + tsiniva);
                    double total = (tsiniva * 0.19) + tsiniva;
                    totaliva.setText("" + total);
                }
            }
        }
    }//GEN-LAST:event_bminimizar2MouseClicked

